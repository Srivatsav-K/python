first_number = int(input('Enter the first number : '))
second_number = int(input('Enter the second number : '))

print(
    f'The addition of {first_number} and {second_number} is : {first_number + second_number}')
print(
    f'The subtraction of {first_number} and {second_number} is : {first_number - second_number}')
print(
    f'The multiplication of {first_number} and {second_number} is : {first_number * second_number}')
print(
    f'The division of {first_number} and {second_number} is : {first_number / second_number}')
print(
    f'The floored division of {first_number} and {second_number} is : {first_number // second_number}')
print(
    f'The {second_number}th power of  {first_number} is : {first_number ** second_number}')
